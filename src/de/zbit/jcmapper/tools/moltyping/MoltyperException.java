package de.zbit.jcmapper.tools.moltyping;

public class MoltyperException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4846173407642299771L;

	public MoltyperException() {
	}

	public MoltyperException(String arg0) {
		super(arg0);
	}

	public MoltyperException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MoltyperException(Throwable arg0) {
		super(arg0);
	}

}
